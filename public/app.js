// Configuration du réseau Mega Testnet
const megaTestnet = {
  chainId: "0x18C6", // 6342 en hexa
  chainName: "Mega Testnet",
  nativeCurrency: {
    name: "MegaETH",
    symbol: "MEGETH",
    decimals: 18
  },
  rpcUrls: ["https://carrot.megaeth.com/rpc"],
  blockExplorerUrls: ["https://megaexplorer.xyz"]
};

// Variables globales
let provider;
let signer;
let contract;
let connectedAddress;

// Initialisation quand la page est chargée
document.addEventListener('DOMContentLoaded', () => {
  initWallet();
  setupEventListeners();
  checkConnectedWallet();
});

// Initialisation du wallet
function initWallet() {
  if (window.ethereum) {
    provider = new ethers.providers.Web3Provider(window.ethereum);
    contract = new ethers.Contract(contractAddress, contractABI, provider);
    
    // Écoute les changements de compte
    window.ethereum.on('accountsChanged', (accounts) => {
      if (accounts.length > 0) {
        connectedAddress = accounts[0];
        updateWalletInfo();
      } else {
        disconnectWallet();
      }
    });
    
    // Écoute les changements de réseau
    window.ethereum.on('chainChanged', (chainId) => {
      if (parseInt(chainId) !== 6342) {
        alert("Please switch to Mega Testnet");
      }
    });
  } else {
    alert("Please install MetaMask or another Web3 wallet");
  }
}

// Vérifie si un wallet est déjà connecté
async function checkConnectedWallet() {
  if (window.ethereum) {
    const accounts = await window.ethereum.request({ method: 'eth_accounts' });
    if (accounts.length > 0) {
      connectedAddress = accounts[0];
      signer = provider.getSigner();
      contract = new ethers.Contract(contractAddress, contractABI, signer);
      updateWalletInfo();
    }
  }
}

// Connecte le wallet
async function connectWallet() {
  try {
    // Vérifie et ajoute le réseau Mega Testnet si nécessaire
    await window.ethereum.request({
      method: 'wallet_addEthereumChain',
      params: [megaTestnet]
    });
    
    // Demande la connexion
    const accounts = await window.ethereum.request({ method: 'eth_requestAccounts' });
    connectedAddress = accounts[0];
    signer = provider.getSigner();
    contract = new ethers.Contract(contractAddress, contractABI, signer);
    
    updateWalletInfo();
    showModal("Wallet Connected", `Successfully connected: ${connectedAddress}`);
  } catch (error) {
    console.error("Error connecting wallet:", error);
    showModal("Connection Error", error.message);
  }
}

// Déconnecte le wallet
function disconnectWallet() {
  connectedAddress = null;
  signer = null;
  document.getElementById('wallet-info').classList.add('hidden');
  document.getElementById('connect-wallet').classList.remove('hidden');
  document.getElementById('wallet-details').innerHTML = '<p>Connect your wallet to interact with TAAMS</p>';
}

// Met à jour les infos du wallet dans l'UI
async function updateWalletInfo() {
  document.getElementById('wallet-info').classList.remove('hidden');
  document.getElementById('connect-wallet').classList.add('hidden');
  
  const shortAddress = `${connectedAddress.substring(0, 6)}...${connectedAddress.substring(38)}`;
  document.getElementById('wallet-address').textContent = shortAddress;
  
  // Récupère le solde
  const balance = await provider.getBalance(connectedAddress);
  const balanceInEth = ethers.utils.formatEther(balance);
  
  document.getElementById('wallet-details').innerHTML = `
    <p><span class="text-gray-400">Address:</span> ${shortAddress}</p>
    <p><span class="text-gray-400">Balance:</span> ${parseFloat(balanceInEth).toFixed(4)} MegaETH</p>
  `;
}

// Configure les écouteurs d'événements
function setupEventListeners() {
  document.getElementById('connect-wallet').addEventListener('click', connectWallet);
  
  document.getElementById('get-balance').addEventListener('click', async () => {
    if (!connectedAddress) {
      showModal("Error", "Please connect your wallet first");
      return;
    }
    
    try {
      const balance = await contract.balanceOf(connectedAddress);
      showModal("Your Balance", `You have ${ethers.utils.formatEther(balance)} TAAMS tokens`);
    } catch (error) {
      showModal("Error", error.message);
    }
  });
  
  document.getElementById('stake-btn').addEventListener('click', async () => {
    if (!connectedAddress) {
      showModal("Error", "Please connect your wallet first");
      return;
    }
    
    const amount = prompt("Enter amount to stake:");
    if (amount && !isNaN(amount)) {
      try {
        showModal("Processing", "Please confirm the transaction in your wallet");
        const tx = await contract.stake(ethers.utils.parseEther(amount));
        await tx.wait();
        showModal("Success", `Successfully staked ${amount} TAAMS tokens`);
      } catch (error) {
        showModal("Error", error.message);
      }
    }
  });
  
  document.getElementById('claim-rewards').addEventListener('click', async () => {
    if (!connectedAddress) {
      showModal("Error", "Please connect your wallet first");
      return;
    }
    
    try {
      showModal("Processing", "Please confirm the transaction in your wallet");
      const tx = await contract.claimBL2PReward();
      await tx.wait();
      showModal("Success", "Rewards claimed successfully!");
    } catch (error) {
      showModal("Error", error.message);
    }
  });
  
  document.getElementById('close-modal').addEventListener('click', () => {
    document.getElementById('transaction-modal').classList.add('hidden');
  });
}

// Affiche une modal
function showModal(title, content) {
  document.getElementById('modal-title').textContent = title;
  document.getElementById('modal-content').innerHTML = `<p>${content}</p>`;
  document.getElementById('transaction-modal').classList.remove('hidden');
}